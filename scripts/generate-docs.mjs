// Write each document
const indexEntries = [];
for (const doc of docs) {
  const dir = path.join(DOCS_DIR, doc.category);
  const filename = `${doc.id}_${doc.title}.md`;
  const filepath = path.join(dir, filename);

  const frontmatter = [
    "---",
    `id: "${doc.id}"`,
    `title: "${doc.title}"`,
    `category: "${doc.category}"`,
    `source_format: "original"`,
    `created_date: "2024-01-15"`,
    `last_updated: "2024-12-01"`,
    `version: "1.0"`,
    `keywords: [${doc.keywords.map((k) => `"${k}"`).join(", ")}]`,
    `summary: "${doc.summary}"`,
    `sections:`,
    ...doc.sections.map((s) => `  - id: "${s.id}"\n    title: "${s.title}"`),
    "---",
    "",
  ].join("\n");

  const body = doc.sections
    .map((s) => `## ${s.title}\n<!-- section_id: ${s.id} -->\n\n${s.body}\n`)
    .join("\n");

  fs.writeFileSync(filepath, frontmatter + "\n" + body, "utf8");

  indexEntries.push({
    id: doc.id,
    title: doc.title,
    category: doc.category,
    path: path.relative(ROOT, filepath),
    keywords: doc.keywords,
    summary: doc.summary,
    sections: doc.sections.map((s) => ({ id: s.id, title: s.title })),
  });
}

fs.writeFileSync(
  path.join(DOCS_DIR, "index.json"),
  JSON.stringify(indexEntries, null, 2),
  "utf8"
);


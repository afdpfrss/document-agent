function categorize(question: string): Category {
  for (const [cat, re] of KEYWORDS) {
    if (re.test(question)) return cat;
  }
  return "default";
}

export function pickIntro(question: string): string {
  const pool = PHRASES[categorize(question)];
  return pool[Math.floor(Math.random() * pool.length)];
}
